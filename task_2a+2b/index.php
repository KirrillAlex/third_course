<!-- Изучение PHP для начинающих | Урок #9 - Логические операции -->
<?php
    $x = 12;//объявляем переменную x
    $y = 34;//объявляем переменную y
    $z = 4.25;//объявляем переменную z

    $bool_1 = $x != $z;//bool_1 = 1, если х не равен у, иначе 0

    $bool_2 = $x <= $y;//bool_2 = 1, если х меньше или равен у, иначе 0

    echo $bool_1."<br>".$bool_2."<hr>";//вывод первых 2х логических переменных

    $bool_3 = !($x == $y);//bool_3 = 1, если х не равен у, иначе 0

    $bool_4 = $x == $y || $z < $y;//bool_4 = 1, если х равен у или z меньше y, иначе 0

    $bool_5 = $z != $y && $x < $z;//bool_5 = 1, если z не равен у и х меньше z, иначе 0

    $bool_6 = $z != $x ^ $y < $z;//bool_6 = 1, если z не равен х и у больше z и наоборот, иначе 0

    echo "<br>".$bool_3."<br>".$bool_4."<br>".$bool_5."<br>".$bool_6;//вывод оставшихся 4х логических переменных

    $bool = !($x != $y && $z < $x) || $x != $y;//bool = 1, (если х не равен у и z меньше х) = false или х не равен у

    echo "<br>$bool = !($x != $y && $z < $x) || $x != $y";//вывод результата
?>



