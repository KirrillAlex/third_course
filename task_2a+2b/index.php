<!-- Изучение PHP для начинающих | Урок #13 - Циклы For, While и Do While -->
<?php
    for ($i = 100; $i >= 80; $i -= 2){//запускаем цикл от 100 до 80, уменьшая значение y на каждом шаге на 2
        if($i % 5 == 0) continue;//если остаток от деления на 5 нулевой, идем далее по телу цикла
        if($i <= 87) break;//если число меньше 87, выходим из цикла
        echo "Цикл под номером № $i <br>";//выводим текст и значение i, если дойдем до этой строки
    }

    echo "<br>";//перевод на новую строку

    $x = 1;//объявляем переменную х

    while ($x <= 10){//запускаем цикл с условием "покаа х меньше либо равен 10"
        echo "Итерация под номером № $x <br>";//выводим текст и значение х
        if($x == 7)//если х равен 7 запускаем цикл от 0 до 5 с шагом 3
        for ($y = 0; $y <5; $y += 3)
            echo "Ещё одна итерация - $y <br>";//вывод текста и значения у
        $x++;
    }

    $z = 10;//объявляем переменную z

    echo "<br>";//перевод на новую строку

    do {// цикл выполняется, пока z меньше 5
        echo "Операция выполнилась только один раз";//вывод текста
        $z++;//увеличение z на единицу
    } while ($z < 5);//само условие
?>